package streams.filtrar;

public class Produtos {

	String nome;
	double preco;
	double desconto;
	boolean freteGratis;
	public Produtos(String nome, double preco, double desconto, boolean freteGratis) {
		super();
		this.nome = nome;
		this.preco = preco;
		this.desconto = desconto;
		this.freteGratis = freteGratis;
	}
	
	
	
}
